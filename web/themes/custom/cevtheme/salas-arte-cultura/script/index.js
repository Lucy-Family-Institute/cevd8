function setSceneHeight() {
  const numberOfItems = pistas.length;
  const itemZ = parseFloat(window.getComputedStyle(document.body).getPropertyValue("--itemZ"));
  const scenePerspective = parseFloat(window.getComputedStyle(document.body).getPropertyValue("--scenePerspective"));
  const cameraSpeed = parseFloat(window.getComputedStyle(document.body).getPropertyValue("--cameraSpeed"));
  const height = window.innerHeight + scenePerspective * cameraSpeed + itemZ * cameraSpeed * numberOfItems;
  document.body.style.setProperty("--viewportHeight", height);
}
