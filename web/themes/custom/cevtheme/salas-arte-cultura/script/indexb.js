function filtrosimag(filtro){
    var ruta = '';
    switch(filtro){
        case 'audiovisual':ruta = 'imageR1';
        break;
        case 'grafica':ruta = 'imageR2';
        break;
        case 'multiformato':ruta = 'imageR3';
        break;
        case 'sonora':ruta = 'imageR4';
        break; 
        case 'acontecimiento':ruta = 'imageR5';
        break;
        case 'inicio':ruta = 'image';
        break;        
        default:console.log('No hay contenido desarrollado para la opción:' + filtro+ '. Crear contenido dentro de function filtrosimg');
    }  
    remplazoImg(ruta);
}
function remplazoImg(remplazo){
    var imagen = document.querySelectorAll('.card-img-top');
    for(var i=0; i< imagen.length; i++){
        imagen[i].src = pistas[i][remplazo];
        imagen[i].alt = pistas[i][remplazo];
        if(remplazo == 'image'){
            imagen[i].classList.remove('filtroActivo');
        }else{
            imagen[i].classList.add('filtroActivo');
        }
        
    }
}

