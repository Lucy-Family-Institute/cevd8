//funcion que permite descargar y visualizar el pdf creado
function crearpdf(){
  const $pdfCreador = document.getElementById('PDF');
  html2pdf()
            .set({
                margin: 1,
                filename: 'ruta-likes.pdf',
                image: {
                    type: 'jpeg'
                },
                html2canvas: {
                    scale: 5, // A mayor escala, mejores gráficos, pero más peso
                    letterRendering: true,
                },
                jsPDF: {
                    unit: "cm",
                    format: "a3",
                    orientation: 'portrait' // landscape o portrait
                }
            })
            .from($pdfCreador)
            .save()
            .catch(err => console.log(err));
}


