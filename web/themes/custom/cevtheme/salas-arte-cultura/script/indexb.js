function moveCameraAngle(event) {
    if(screen.width < 768){
        var xGap = (((event.clientX - window.innerWidth / 2) * -650) /  (window.innerWidth / 2)) *  -1;
    }else{
        var xGap = (((event.clientX - window.innerWidth / 2) * -50) /  (window.innerWidth / 2)) *  -1;
    }

  
  const yGap =(((event.clientY - window.innerHeight / 2) * 50) / (window.innerHeight / 2)) *  -1;
  const newPerspectiveOriginX =  perspectiveOrigin.x + (xGap * perspectiveOrigin.maxGap) / 100;
  const newPerspectiveOriginY =  perspectiveOrigin.y + (yGap * perspectiveOrigin.maxGap) / 100;

  document.documentElement.style.setProperty(
    "--scenePerspectiveOriginX",
    newPerspectiveOriginX
  );
  document.documentElement.style.setProperty(
    "--scenePerspectiveOriginY",
    newPerspectiveOriginY
  );
}

