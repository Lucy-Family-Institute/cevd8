//funcion de creación de elementos PDF de acuerdo con LIKE y DISLIKE
function pdfInsertElemento(valor){
  var existe = 0;
 if(pdfElementos.length>0){
   for(var i=0;i<pdfElementos.length; i++){
     if(pdfElementos[i].id === valor.id){
       existe =1;
       i=pdfElementos.length;
     }else{
       existe =0;
     }
   }
   if(existe == 0){
     for(var i =0; i<pistas.length; i++){
       if(pistas[i].id === valor.id){
         insertElemenPdf(pistas[i].id, pistas[i].title, pistas[i].movie_banner, pistas[i].description,pistas[i].musical);
       }
     }
   }
   if(existe ==1){
     removerElementpdf(valor.id);
   }

 } else{
   for(var j=0; j< pistas.length; j++){
    if(pistas[j].id == valor.id){
       pdfElementos.push({
         id:pistas[j].id,
         titulo:pistas[j].title,
         imagen:pistas[j].movie_banner,
         descripcion:pistas[j].description,
         musical:pistas[j].musical
       });
     }
   }
 }
 document.querySelector('.fijobadge').innerHTML = pdfElementos.length;
}
